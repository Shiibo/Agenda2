    private void BtT10AnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtT10AnteriorActionPerformed
        if (cont < 1) {
        cont = bd.size()-1;
        } else {
        cont--;
        }
        DisableEdit();
        AttContatos();
        LabelT10IDN.setText(String.valueOf(this.bd.get(cont).getId()));
    }//GEN-LAST:event_BtT10AnteriorActionPerformed

    private void BtT10ProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtT10ProximoActionPerformed
        if (cont > bd.size()-2){
        cont = 0;
        } else {
        cont++;
        }
        DisableEdit();
        AttContatos();
        LabelT10IDN.setText(String.valueOf(this.bd.get(cont).getId()));
    }//GEN-LAST:event_BtT10ProximoActionPerformed

    private void BtT10ApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtT10ApagarActionPerformed
        String a = this.TextT10Nome.getText();
        int resposta = JOptionPane.showConfirmDialog( null,"Confirma a exclusão de "+a+" ?", "Exclusão de Compromisso",JOptionPane.YES_NO_OPTION);
        
        if(resposta == 0){
            AgendaDAO.Delete(this.bd.get(cont).getId());
        
            bd = (ArrayList<Contato>) getContato();
            AttContatos();
        }
    }//GEN-LAST:event_BtT10ApagarActionPerformed

