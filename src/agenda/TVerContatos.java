    private void BtT10SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtT10SalvarActionPerformed
        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
        java.sql.Date data = null;
        try {
            data = new java.sql.Date(formato.parse(this.TextT10DtNasc.getText()).getTime());
        } catch (ParseException ex) {
            Logger.getLogger(TContato.class.getName()).log(Level.SEVERE, null, ex);
        }

        AgendaDAO agendaDao = new AgendaDAO();
        Contato c = new Contato();

        //c.setId();
        this.bd.get(cont).setNome(TextT10Nome.getText());
        this.bd.get(cont).setNascimento(data);
        this.bd.get(cont).setEndereco(TextT10Endereco.getText());
        this.bd.get(cont).setTelefone(TextT10Telefone.getText());
        this.bd.get(cont).setEmail(TextT10Email.getText());
        
        c.setId(Integer.parseInt(this.LabelT10IDN.getText()));
        c.setNome(this.TextT10Nome.getText());
        c.setNascimento(data);
        c.setEndereco(this.TextT10Endereco.getText());
        c.setTelefone(this.TextT10Telefone.getText());
        c.setEmail(this.TextT10Email.getText());

        AgendaDAO.Update(c);
        
        DisableEdit();
    }//GEN-LAST:event_BtT10SalvarActionPerformed

